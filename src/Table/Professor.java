package Table;

public class Professor {
    public int professor_id;
    public String professor_name;
    public String professor_password;
    public String professor_subject;

    public int getProfessor_id() {
        return professor_id;
    }

    public void setProfessor_id(int professor_id) {
        this.professor_id = professor_id;
    }

    public String getProfessor_name() {
        return professor_name;
    }

    public void setProfessor_name(String professor_name) {
        this.professor_name = professor_name;
    }

    public String getProfessor_password() {
        return professor_password;
    }

    public void setProfessor_password(String professor_password) {
        this.professor_password = professor_password;
    }

    public String getProfessor_subject() {
        return professor_subject;
    }

    public void setProfessor_subject(String professor_subject) {
        this.professor_subject = professor_subject;
    }


}
